package com.im.bo;

public class AdminBO {
	private String userName;
	private String name;
	private String user_type;
	private String login_token;
	private String user_pin;
	private String seq_question;
	private String seq_answer;
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUser_type() {
		return user_type;
	}
	public void setUser_type(String user_type) {
		this.user_type = user_type;
	}
	public String getLogin_token() {
		return login_token;
	}
	public void setLogin_token(String login_token) {
		this.login_token = login_token;
	}
	public String getUser_pin() {
		return user_pin;
	}
	public void setUser_pin(String user_pin) {
		this.user_pin = user_pin;
	}
	public String getSeq_question() {
		return seq_question;
	}
	public void setSeq_question(String seq_question) {
		this.seq_question = seq_question;
	}
	public String getSeq_answer() {
		return seq_answer;
	}
	public void setSeq_answer(String seq_answer) {
		this.seq_answer = seq_answer;
	}
	
	

}
